package com.google.cloud.verticals.foundations.dataharmonization.function.java;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Labels this function to be wrapped in an {@link JavaFunction} by {@link
 * JavaFunction#ofPluginFunctionsInClass(Class)}. Must be on a public static method.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface PluginFunction {
  boolean inheritParentVars() default false;
}
